    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // parcours:
        String ordre = (String) jComboBox1.getSelectedItem();
        if (ordre.equals("pre-order")) {
            String text = jTextArea1.getText() + "\n";
            jTextArea1.setText(text + "Pre-order traversal initiated:");
            tree.preorderTraversal(tree.root());
            
        }
        
         if (ordre.equals("order")) {
            String text = jTextArea1.getText() + "\n";
            jTextArea1.setText(text + "Order traversal initiated:");
            tree.orderTraversal(tree.root());
        }
         
        if (ordre.equals("post-order")) {
            String text = jTextArea1.getText() + "\n";
            jTextArea1.setText(text + "Post-order traversal initiated:");
            tree.orderTraversal(tree.root());
        }
    }//GEN-LAST:event_jButton7ActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // création de l'arbre
        if (jRadioButton1.isSelected()) {
            this.tree = new BinaryTree();
            this.tree.register(this);
            treePanel2.setTree(tree);
            this.tree.register(treePanel2);
            String text = jTextArea1.getText() + "\n";
            jTextArea1.setText(text + "New binary tree created");
           
        } else {
            this.tree = new TreeRB();
            this.tree.register(this);
            treePanel2.setTree(tree);
            this.tree.register(treePanel2);
            String text = jTextArea1.getText() + "\n";
            jTextArea1.setText(text + "New red-black tree created");
        
        }

    }//GEN-LAST:event_jButton1ActionPerformed

